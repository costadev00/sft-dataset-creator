from __future__ import annotations

import os
from pathlib import Path

from datasets import Dataset, load_dataset
from huggingface_hub import HfApi, create_repo, upload_folder
from rich.console import Console

from wiki_if_builder.utils import line_count, safe_size


console = Console()


def validate_dataset_publish_dir(local_dir: str | Path) -> Path:
    path = Path(local_dir)
    if not path.exists() or not path.is_dir():
        raise FileNotFoundError(f"Diretório de dataset não existe: {path}")
    if not (path / "README.md").exists():
        raise FileNotFoundError(f"README.md ausente em {path}")
    if not (path / "data.jsonl").exists() and not (path / "data.parquet").exists():
        raise FileNotFoundError(f"data.jsonl ou data.parquet ausente em {path}")
    return path


def build_hf_dataset_from_jsonl(jsonl_path: str) -> Dataset:
    path = Path(jsonl_path)
    if not path.exists():
        raise FileNotFoundError(f"JSONL não encontrado: {path}")
    return load_dataset("json", data_files=str(path), split="train")


def dataset_publish_stats(local_dir: str | Path) -> dict[str, object]:
    path = validate_dataset_publish_dir(local_dir)
    data_path = path / "data.jsonl"
    parquet_path = path / "data.parquet"
    primary_data = data_path if data_path.exists() else parquet_path
    return {
        "local_dir": str(path),
        "line_count": line_count(data_path) if data_path.exists() else None,
        "data_file": str(primary_data),
        "data_size_bytes": safe_size(primary_data),
        "total_size_bytes": safe_size(path),
    }


def publish_dataset(
    local_dir: str,
    repo_id: str,
    private: bool = True,
    commit_message: str = "Upload dataset generated by wiki-if-builder",
) -> None:
    token = os.getenv("HF_TOKEN")
    if not token:
        raise EnvironmentError("HF_TOKEN ausente. Configure HF_TOKEN antes de publicar no Hugging Face Hub.")

    path = validate_dataset_publish_dir(local_dir)
    stats = dataset_publish_stats(path)
    console.print("[bold]Preparando publicação no Hugging Face Hub[/bold]")
    console.print(f"repo_id: {repo_id}")
    console.print(f"private: {private}")
    console.print(f"linhas: {stats['line_count']}")
    console.print(f"data.jsonl/data.parquet: {stats['data_size_bytes']} bytes")
    console.print(f"tamanho total do diretório: {stats['total_size_bytes']} bytes")

    create_repo(repo_id=repo_id, repo_type="dataset", private=private, exist_ok=True, token=token)
    upload_folder(
        repo_id=repo_id,
        repo_type="dataset",
        folder_path=str(path),
        commit_message=commit_message,
        token=token,
    )
    api = HfApi(token=token)
    info = api.dataset_info(repo_id)
    console.print(f"[green]Publicação concluída:[/green] {info.id}")

